use bytes::Bytes;

use crate::{encoding::wire::Parser, error::Error};

pub struct Deserializer {
    parser: Parser,
}

impl Deserializer {
    pub fn new(buf: Bytes) -> Self {
        Deserializer {
            parser: Parser::new(buf),
        }
    }
}

